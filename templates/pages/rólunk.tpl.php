            Duna-Mecsek Területfejlesztési Alapítvány bemutatkozás </p>
        <div class="articleContent">
            <p><strong>Az Alap&iacute;tv&aacute;ny tev&eacute;kenys&eacute;ge</strong><br />
                <img style="float: left; border: 2px solid #FFB58C; margin: 5px;" src="./images/dsc_0062_t.jpg" alt="">
                A Paksi Atomerőmű Zrt. által létrehozott Duna-Mecsek Területfejlesztési Alapítvány 2006 áprilisában kezdte meg működését. Az alapítvány célja a település-,
                térség- és gazdaságfejlesztés. Az alapító Paksi Atomerőmű Zrt. évente 500 millió forintot bocsát az alapítvány rendelkezésére, ezt a támogatást öt évre
                garantálta. Az alapítvány a hazai nukleáris létesítmények környezetében, továbbá a hulladéktároló-helyek kutatásában érintett településkörben működik,
                nevezetesen Paks, Kalocsa, Bátaapáti, Boda és Püspökszilágy körzetében. A felsorolt térségekben hatvan település található, melyek összlélekszáma 136 ezer fő.
                Az érintett települések önkormányzatai, azok társulásai, továbbá a közigazgatási területükön működő intézmények, közhasznú szervezetek, civilszervezetek,
                egyesületek és gazdálkodó szervezetek kaphatnak pályázati úton segítséget mindazon tevékenységükhöz, amelyek az Alapítvány céljaival egyértelműen összhangban
                állnak.
                <img style="float: right; border: 2px solid #FFB58C; margin: 5px;" src="./images/dsc_4511_t.jpg" alt="" />
                A Kuratórium a 2009. március 12-i ül
                ésén elfogadta az ez évi pályázati felhívást, amelyben az elmúlt évek tapasztalatait, valamint a világgazdaságban kialakult recessziót is figyelembe véve
                jól átlátható, egyszerűen kezelhető, transzparens rendszert fogalmaztak meg. Az ebben felállított prioritások kiemelten gazdaságfejlesztésre,
                munkahelyteremtésre, települések összefogását, valamint a turisztikai vonzerő fejlesztését elősegítő, ill. környezetvédelmi programokra terjednek ki. Az
                alapítvány az európai uniós forrásokból magyar társfinanszírozás mellett támogatott operatív programok pályázataiban való részvételt is támogatni kívánja.
                A támogatás lehetséges formája a pályázatokhoz szükséges önrész meghatározott részének biztosítása. Ezen prioritásokat külön is megfogalmazták az
                önkormányzatok, azok társulásai, intézmények, közhasznú szervezetek, civilszervezetek, egyesületek és gazdálkodó szervezetek számára. <br /><br />A
                pályázati felhívás a pályázók teljes köre számára lehetőséget biztosít az EGT és Norvég Finanszírozási Mechanizmusok valamint az alapítvány támogatásával
                megvalósuló "Akadálymentesítések megvalósításának támogatása a Duna-Mecsek Alapítvány által érintett települések területén" tárgyú pályázat előkészítési
                költségeinek finanszírozására. Az alapítvány ez évben lehetőséget biztosít a kis közösségek energiaracionalizálási tevékenységének támogatására is. <br />
                <br />A támogatások összegének nagyságát és a támogatások mértékét a szakmai szempontok messzemenő figyelembevételével fogalmazták meg, összhangban a
                rendelkezésre álló forrásokkal és a várható igényekkel. <br /><br /><br />
