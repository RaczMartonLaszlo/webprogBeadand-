        <h3>
            Sajtóközlemény - kuratórium 2009. december 17-ei üléséről
        </h3>

        <span class="date">2009-12-21 </span>
        <p class="lead">
            Az Alapítvány 300 paksi munkahely megtartásához ad támogatást A Duna-Mecsek Területfejlesztési Alapítvány kuratóriumának 2009. december 17-ei ülésén a 2009. évi működés eredményeinek értékelése mellett támogatás odaítéléséről is határozott, amely döntésével közel 300 paksi munkahely fennmaradásához járult hozzá. A támogatás odaítélésére a paksi székhelyű F6 Ruhaipari Kereskedelmi és Szolgáltató Kft. pályázati kérelme alapján került sor.
